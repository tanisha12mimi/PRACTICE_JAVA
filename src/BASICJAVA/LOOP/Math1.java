package BASICJAVA.LOOP;

public class Math1 {
    public static void main(String[] args) {
        // find the max and min

        System.out.println(java.lang.Math.max(45,78)); //78
        System.out.println(java.lang.Math.min(34,101)); //34

        // Find the sqrt

        System.out.println(java.lang.Math.sqrt(625)); //25

        // absolute value
        //it converts negative number into positive number
        System.out.println(java.lang.Math.abs(-78)); //+78

        // Find out the power
        // First number should the number for which we need to find the power
        // the second number should be the value of the power
        System.out.println(java.lang.Math.pow(8,4)); // 8^4
        System.out.println(java.lang.Math.pow(9,4)); // 9^4

    }
}
